// CREATING OBJECTS
// object literal IS A SIMPLE WAY TO DEFINE AN OBJECT
const circle ={
    radius: 1,
    location: {
        x:1,
        y:1
    },
    draw: function(){
        console.log('draw');
    }  
}
circle.draw();

